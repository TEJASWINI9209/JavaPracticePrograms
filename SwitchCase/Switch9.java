class Switch9 
{
	public static void main(String[] args) 
	{
		char x= 'a';

		switch(x){
			case "Cyber" : System.out.println("Inside Cyber");
			break;

			case "Success": System.out.println("Inside Success");
			break;

			default : System.out.println("Inside default");
		}

	}
}


// it converts 97 to its ascii value i.e 'a'=97