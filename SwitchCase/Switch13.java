class Switch13
{
	public static void main(String[] args) 
	{
		int a=10;
		switch (a)
		{
		case 12 :
		         System.out.println("Hello Tejaswini!");
				 break;
		default:
				System.out.println("Hello World!");
				break;
		case 11: 
				System.out.println("Hello Mansi!");
				break;

		}
	}
}


// We can write the default case anywhere within the switch stmt, hence it is not mandatory default stmt will be the last stmt.