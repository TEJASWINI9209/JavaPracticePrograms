class Switch1 
{
	public static void main(String[] args) 
	{
		int a=4;
		switch(a){
			case 1:  System.out.println("inside one statement");
					 break;

			case 2:  System.out.println("inside two statement");
					 break;

			default: System.out.println("inside default statement");
			         break;
			
		}

		
	}
}
