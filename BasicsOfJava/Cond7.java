class Cond7 
{
	public static void main(String[] args) 
	{
		int num1=700, num2=1000;
		if (num1>num2)
		{
			System.out.println("Num1 is greater");
		}
		else if(num1<num2){
			System.out.println("Num2 is greater ");
		}
		else if(num1==num2){
			System.out.println("Num1 is equal to num2");
		}
		else{
			System.out.println("Invalid number");
		}
	
	}
}
