class For4
{
	public static void main(String[] args) 
	{
		int i=1;
		for (System.out.println("One"),System.out.println("Two") ; i<=5 ; i++)  /*within the for stmt in place of initialization and declaration
																					we can use one or more stmts */
		{
			System.out.println("The value of i is "+ i);
		}
		
	}
}
